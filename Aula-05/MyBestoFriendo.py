Idade = int(input("Digite sua idade: "))

if Idade >= 18:
            print(f"Você é maior de idade, pois já tem {Idade}") 
elif Idade >= 14:               
            print (f"Você é menor de idade, pois ainda tem {Idade}")  
else: 
            print(f"Você ainda é uma criança, pois tem {Idade}")            